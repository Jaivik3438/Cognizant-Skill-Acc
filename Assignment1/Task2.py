# Task 2 - Operators: Playing with Numbers
# Defining two numbers to work with
num1 = 30
num2 = 20

# Performing addition and printing the result
print(f"The addition of {num1} and {num2} is", num1 + num2)
print("--------------------------------------------------------------")

# Performing subtraction and printing the result
print(f"The subtraction of {num1} and {num2} is", num1 - num2)
print("--------------------------------------------------------------")

# Performing multiplication and printing the result
print(f"The multiplication of {num1} and {num2} is", num1 * num2)
print("--------------------------------------------------------------")

# Performing division and printing the result
print(f"The division of {num1} and {num2} is", num1 / num2)
print("--------------------------------------------------------------")

# Performing floor division and printing the result
print(f"The floor division of {num1} and {num2} is", num1 // num2)
print("--------------------------------------------------------------")

# Performing modulo and printing the result (remainder after division)
print(f"The modulo of {num1} and {num2} is", num1 % num2)
print("--------------------------------------------------------------")


